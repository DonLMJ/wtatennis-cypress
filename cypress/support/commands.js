import { urls } from './helpers';

Cypress.Commands.add('interceptRequest', (name) => {
    if (!urls[name]) {
        throw new Error(`No URL mapping found for intercept name: ${name}`);
    }

    cy.intercept('GET', urls[name]).as(name);
});